# python program to convert octal, binary and hexadecimal numbers into decimal
c1 = 0o10
c2 = 0B1110010
c3 = 0x1c2

n = int(c1)
print("Octal to Decimal: ", n)
n = int(c2)
print("Binary to Decimal: ", n)
n = int(c3)
print("Hexadecimal to Decimal: ", n)
# int() function converts the given number into integer